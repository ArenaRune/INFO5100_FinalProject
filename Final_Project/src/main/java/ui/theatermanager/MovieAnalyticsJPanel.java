    private void btnPerformanceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPerformanceActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblMovieHistory.getSelectedRow();
        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Please select a movie.");
            return;
        }
        
        Movie movie = (Movie) tblMovieHistory.getValueAt(selectedRowIndex, 0);
        
        txtTotalRevenue.setText(String.valueOf(movie.getRevenue()));
        txtTheaterRevenue.setText(String.valueOf(t.getRevenueList().get(movie)));
        txtTotalTickets.setText(String.valueOf(movie.getTicketCount()));
        txtTheaterTickets.setText(String.valueOf(t.getTicketsSold().get(movie)));
    }//GEN-LAST:event_btnPerformanceActionPerformed


