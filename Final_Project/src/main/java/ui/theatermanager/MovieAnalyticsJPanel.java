    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblMovieHistory.getModel();
        model.setRowCount(0);
        
        for (Movie m: t.getMovieList()) {
            Object row[] = new Object[1];
            row[0] = m;
            model.addRow(row);
        }
    }
