    private void btnRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRequestActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblTicketHistory.getSelectedRow();
        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Please select a Ticket.");
            return;
        }
        
        Ticket ticket = (Ticket) tblTicketHistory.getValueAt(selectedRowIndex, 0);
        
        RefundWorkRequest rwr = CustomerSupportRole.newRefundWorkRequest();
        rwr.setSender(cr);
        rwr.setTicket(ticket);
        rwr.setStatus("Pending");
        cr.getWorkQueue().getWorkQueue().add(rwr);
        populateRequestTable();
        
        DB4OUtil.getInstance().storeSystem(system);
    }//GEN-LAST:event_btnRequestActionPerformed


