    private void populateTicketTable() {
        DefaultTableModel model = (DefaultTableModel) tblTicketHistory.getModel();
        model.setRowCount(0);
        
        for (Ticket t : cr.getTickets()) {
            Object row[] = new Object[2];
            row[0] = t;
            row[1] = t.getShow().getTheater();
            model.addRow(row);
        }
    }

    private void populateRequestTable() {
        DefaultTableModel model = (DefaultTableModel) tblRefundStatus.getModel();
        model.setRowCount(0);
        
        ArrayList<RefundWorkRequest> requests = new ArrayList<RefundWorkRequest>();
        for (WorkRequest wr : cr.getWorkQueue().getWorkQueue()) {
            requests.add((RefundWorkRequest) wr);
        }
        
        for (RefundWorkRequest rwr : requests) {
            Object row[] = new Object[3];
            row[0] = rwr.getTicket().getShow().getMovie();
            row[1] = rwr.getTicket().getShow().getTheater().getName();
            row[2] = rwr.getStatus();
            model.addRow(row);
        }
    }
