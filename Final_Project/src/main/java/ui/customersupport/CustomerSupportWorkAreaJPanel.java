    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblWorkRequets.getModel();
        model.setRowCount(0);
        
        for (RefundWorkRequest rwr : CustomerSupportRole.getRefundWorkQueue()) {
            Object row[] = new Object[3];
            row[0] = rwr;
            row[1] = rwr.getTicket().getShow().getMovie();
            row[2] = rwr.getTicket().getPrice();
            model.addRow(row);
        }
    }
