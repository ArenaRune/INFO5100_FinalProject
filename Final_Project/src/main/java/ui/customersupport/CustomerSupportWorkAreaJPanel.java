    private void btnAcceptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAcceptActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblWorkRequets.getSelectedRow();
        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Please select a work request.");
            return;
        }
        
        RefundWorkRequest rwr = (RefundWorkRequest) tblWorkRequets.getValueAt(selectedRowIndex, 0);
        
        rwr.setStatus("Accepted");
        Ticket ticket = rwr.getTicket();
        Show show = ticket.getShow();
        Movie movie = show.getMovie();
        Theater theater = show.getTheater();
        double price = ticket.getPrice();
        for (Seat s : ticket.getSeats()) {
            s.setIsBooked(false);
        }
        
        theater.getRevenueList().put(movie, theater.getRevenueList().get(movie) - price);
        movie.setRevenue(movie.getRevenue() - price);
        CustomerRole cr = (CustomerRole) rwr.getSender();
        cr.getTickets().remove(ticket);
        theater.getTicketsSold().put(movie, theater.getTicketsSold().get(movie) - ticket.getSeats().size());
        movie.setTicketCount(movie.getTicketCount() - ticket.getSeats().size());
        CustomerSupportRole.getRefundWorkQueue().remove(rwr);
        populateTable();
        
        DB4OUtil.getInstance().storeSystem(system);
    }//GEN-LAST:event_btnAcceptActionPerformed

    private void btnDeclineActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeclineActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblWorkRequets.getSelectedRow();
        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Please select a work request.");
            return;
        }
        
        RefundWorkRequest rwr = (RefundWorkRequest) tblWorkRequets.getValueAt(selectedRowIndex, 0);
        CustomerSupportRole.getRefundWorkQueue().remove(rwr);
        rwr.setStatus("Declined");
        populateTable();
        
        DB4OUtil.getInstance().storeSystem(system);
    }//GEN-LAST:event_btnDeclineActionPerformed


