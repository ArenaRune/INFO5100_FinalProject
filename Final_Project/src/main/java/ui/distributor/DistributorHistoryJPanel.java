    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblHistory.getModel();
        model.setRowCount(0);
        
        for (Movie m: dr.getMovieList()) {
            Object row[] = new Object[3];
            
            row[0] = m;
            row[1] = dr.getCost().get(m);
            row[2] = m.getRevenue();
            
            model.addRow(row);
        }
    }
