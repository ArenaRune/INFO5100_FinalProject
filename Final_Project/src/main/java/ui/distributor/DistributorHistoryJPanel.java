    private void btnPerformanceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPerformanceActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblHistory.getSelectedRow();
        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Please select a movie.");
            return;
        }
        
        Movie movie = (Movie) tblHistory.getValueAt(selectedRowIndex, 0);
        
        txtMargin.setText(String.valueOf(movie.getRevenue() - dr.getCost().get(movie)));
        txtTickets.setText(String.valueOf(movie.getTicketCount()));
    }//GEN-LAST:event_btnPerformanceActionPerformed


